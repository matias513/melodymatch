export type SongSeed = {
  title: string;
  artist: string;
  country: string;
  region: string;
  fingerprint: [number, number, number, number];
};

type RawSongSeed = [
  title: string,
  artist: string,
  country: string,
  region: string,
  fingerprint: [number, number, number, number]
];

function clamp(value: number): number {
  return Math.min(1, Math.max(0, value));
}

function normalizeFingerprint(
  fingerprint: [number, number, number, number]
): [number, number, number, number] {
  return [
    clamp(fingerprint[0]),
    clamp(fingerprint[1]),
    clamp(fingerprint[2]),
    clamp(fingerprint[3]),
  ];
}

function buildCatalog(rows: RawSongSeed[]): SongSeed[] {
  const seen = new Set<string>();

  return rows
    .map(([title, artist, country, region, fingerprint]) => ({
      title: title.trim(),
      artist: artist.trim(),
      country: country.trim(),
      region: region.trim(),
      fingerprint: normalizeFingerprint(fingerprint),
    }))
    .filter((song) => {
      const key = `${song.title.toLowerCase()}__${song.artist.toLowerCase()}`;
      if (seen.has(key)) return false;
      seen.add(key);
      return true;
    });
}

